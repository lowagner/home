int is_plant(W w) {
    if (w.shape == S_PLANT || -w.shape == S_PLANT)
        return 1;
    return 0;
}

int is_obstacle(W w, int dir) {
    if (w.value == 0 || is_plant(w) || w.material == M_WATER) 
        return 0;
    if (w.material == M_CLOUD) {
        if (dir & D_PY)
            return 1;
        return 0;
    }
    return 1;
}

int is_transparent(W w) {
    int shape = w.shape < 0 ? -w.shape : w.shape;
    if (shape == S_CUBE) {
        switch (w.material) {
            case M_GLASS:
            case M_LEAVES:
            //case M_CLOUD:
            case M_WATER:
                return 1;
            default:
                return 0;
        }
    }
    // everything else needs to be treated as see through
    return 1;
}

int is_destructable(W w) {
    if (w.value == 0) // || (w.color == M_CLOUD)) 
        return 0;

    return 1;
}

